import UIKit

// MARK: - Helvetica Neue Ultralight Font

struct HelveticaNeueUltraLight {
    func getFont(size:CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-UltraLight", size: size)!
    }
}